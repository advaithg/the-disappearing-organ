<h3> Class description </h3>
<p>
    This course will introduce students to 3D modeling techniques in the field of cultural heritage conservation. Using the S. Charles Lee Papers, 1919-1962, located in the UCLA Young Research Library Special Collections as a reference, students will, through in-depth archival research, 3D modeling, and augmented reality (AR.), investigate how two of the most significant theatres located in Los Angeles' historic Broadway Theatre district were constructed, decorated, and used. <br/>
    The final project has students in groups investigate how the historic theatres in Downtown LA were designed, constructed, decorated, and used through archival research. That research is demonstrated through 3D, photogrammetric, and AR models. 
</p>
